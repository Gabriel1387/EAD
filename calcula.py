numero = float(input('Digite seu primeiro numero: '))
numero2 = float(input('Digite seu segundo numero: '))

soma = numero + numero2
sub = numero - numero2
div = numero / numero2
mult = numero * numero2

print("Resultado da adição: " + str(soma))
print("Resultado da subtração: " + str(sub))
print("Resultado da divisão: " + str(div))
print("Resultado da multiplicação: " + str(mult))